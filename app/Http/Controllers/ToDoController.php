    {
        $request->validate([
            'note' => ['required'],
        ]);
        $user = ToDo::create([
            'user_id' => Auth::user()->id,
            'description' => $request->note,
            'created_at' => date("Y-m-d h:i:s"),
            'created_by' => Auth::user()->id,
        ]);
        return redirect()->back();
    }

